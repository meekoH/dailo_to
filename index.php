                            <h1>DaiLo <span style="font-weight:400;font-style:italic;color:#603506;">(大佬 \ Cantonese \ noun \ dī-(,)lō):</span></h1>

                            <p>big brother; a term of endearment to show respect and love to an elder; a nod to the ways and tastes of older cultures and generations; implies a connection to future generations. Or, big boss; bad-ass.</p>

                            <p>At DaiLo, we believe eating well is a fundamental part of living well, and how we cook, reflects how we live. Thoughtful and high quality ingredients lead to better lives and more delicious meals.  Our New Asian Cuisine is predominantly Chinese fare but it follows the French tradition. At the heart of the menu is a love and respect for the dishes Chef Nick Liu ate growing up as the son of Hakka parents in Canada, but importantly, they are the dishes his ancestors have been eating for hundreds of years.  Ancient favourites are augmented by Liu’s signature flair and updated to suit the western palate and the modern demand for local and sustainable ingredients.</p>
